    /**
     * 扩展查询：匹配口语词后追加正式法律术语（逐词去重）
     *
     * @param question 预处理后的问题文本
     * @return 扩展后的查询字符串（原始问题 + 去重后的正式术语）
     */
    public String expandQuery(String question) {
        if (question == null || question.isBlank()) {
            return question;
        }

        StringBuilder expanded = new StringBuilder(question);
        java.util.Set<String> addedTerms = new java.util.HashSet<>();

        for (String[] mapping : SYNONYM_MAP) {
            String colloquial = mapping[0];
            String formalTerms = mapping[1];
            if (question.contains(colloquial)) {
                int before = addedTerms.size();
                for (String term : formalTerms.split(" ")) {
                    if (addedTerms.add(term)) {
                        expanded.append(" ").append(term);
                    }
                }
                log.debug("查询扩展: 匹配口语词 '{}' 新增 {} 个术语",
                        colloquial, addedTerms.size() - before);
            }
        }

        String result = expanded.toString().trim();
        if (!result.equals(question)) {
            log.info("查询扩展完成: 原始长度={}, 扩展后长度={}, 新增术语数={}",
                    question.length(), result.length(), addedTerms.size());
        }
        return result;
    }
